package ru.job4j.homeworks.tasks;

//11.14. Вывести элементы массива на экран в обратном порядке.

public class Task1114 {
    public void showArray(int[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(char[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(long[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(double[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(float[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(String[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }

    public void showArray(byte[] arr) {
        for (int i = arr.length; i > 0; i--) {
            System.out.println(arr[i]);
        }
    }
}
